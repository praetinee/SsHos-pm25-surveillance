# Plot 3: Calendar Heatmap
# -------------------------------------
def plot_calendar_heatmap(df_pat, df_pm):
    df_merged = pd.merge(
        df_pat.groupby('‡πÄ‡∏î‡∏∑‡∏≠‡∏ô').size().reset_index(name='count'), 
        df_pm, on='‡πÄ‡∏î‡∏∑‡∏≠‡∏ô', how='inner'
    )
    df_merged['Year'] = pd.to_datetime(df_merged['‡πÄ‡∏î‡∏∑‡∏≠‡∏ô']).dt.year
    df_merged['Month'] = pd.to_datetime(df_merged['‡πÄ‡∏î‡∏∑‡∏≠‡∏ô']).dt.month
    
    # Pivot data for heatmap
    years = sorted(df_merged['Year'].unique(), reverse=True)
    months = ['‡∏°.‡∏Ñ.', '‡∏Å.‡∏û.', '‡∏°‡∏µ.‡∏Ñ.', '‡πÄ‡∏°.‡∏¢.', '‡∏û.‡∏Ñ.', '‡∏°‡∏¥.‡∏¢.', '‡∏Å.‡∏Ñ.', '‡∏™.‡∏Ñ.', '‡∏Å.‡∏¢.', '‡∏ï.‡∏Ñ.', '‡∏û.‡∏¢.', '‡∏ò.‡∏Ñ.']
    
    z_data = [] # PM2.5 for color
    text_data = [] # Patient count for text

    for year in years:
        row_z = []
        row_text = []
        for month_num in range(1, 13):
            cell = df_merged[(df_merged['Year'] == year) & (df_merged['Month'] == month_num)]
            if not cell.empty:
                row_z.append(cell['PM2.5 (ug/m3)'].iloc[0])
                row_text.append(f"{int(cell['count'].iloc[0])}")
            else:
                row_z.append(np.nan)
                row_text.append("")
        z_data.append(row_z)
        text_data.append(row_text)

    fig = go.Figure(data=go.Heatmap(
        z=z_data,
        x=months,
        y=[str(y) for y in years],
        text=text_data,
        texttemplate="%{text}",
        textfont={"size":12},
        colorscale='OrRd',
        hovertemplate='<b>‡∏õ‡∏µ %{y}, ‡πÄ‡∏î‡∏∑‡∏≠‡∏ô %{x}</b><br>PM2.5: %{z:.2f}<br>‡∏ú‡∏π‡πâ‡∏õ‡πà‡∏ß‡∏¢: %{text} ‡∏Ñ‡∏ô<extra></extra>'
    ))

    fig.update_layout(
        title='‡∏õ‡∏è‡∏¥‡∏ó‡∏¥‡∏ô‡πÅ‡∏™‡∏î‡∏á‡∏Ñ‡πà‡∏≤ PM2.5 (‡∏™‡∏µ) ‡πÅ‡∏•‡∏∞‡∏à‡∏≥‡∏ô‡∏ß‡∏ô‡∏ú‡∏π‡πâ‡∏õ‡πà‡∏ß‡∏¢ (‡∏ï‡∏±‡∏ß‡πÄ‡∏•‡∏Ç)'
    )
    st.plotly_chart(fig, use_container_width=True)


