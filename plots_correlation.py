                    df_merged_group,
                    x="PM2.5 (ug/m3)",
                    y=f'‡∏à‡∏≥‡∏ô‡∏ß‡∏ô‡∏ú‡∏π‡πâ‡∏õ‡πà‡∏ß‡∏¢ ({selected_group})',
